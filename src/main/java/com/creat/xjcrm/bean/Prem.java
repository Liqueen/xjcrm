public class Prem {
    private Integer prem_id;

    private String prem_name;

    private String prem_path;

    private Integer prem_parent_id;

    public Integer getPrem_id() {
        return prem_id;
    }

    public void setPrem_id(Integer prem_id) {
        this.prem_id = prem_id;
    }

    public String getPrem_name() {
        return prem_name;
    }

    public void setPrem_name(String prem_name) {
        this.prem_name = prem_name;
    }

    public String getPrem_path() {
        return prem_path;
    }

    public void setPrem_path(String prem_path) {
        this.prem_path = prem_path;
    }

    public Integer getPrem_parent_id() {
        return prem_parent_id;
    }

    public void setPrem_parent_id(Integer prem_parent_id) {
        this.prem_parent_id = prem_parent_id;
    }
}